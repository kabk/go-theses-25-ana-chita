document.addEventListener("DOMContentLoaded", function () {
  console.log("JavaScript Loaded!"); // Debugging

  const images = document.querySelectorAll(".images");
  const fullscreenDiv = document.getElementById("fullscreenImage");
  const fullscreenImg = document.getElementById("fullscreenImg");

  // Check if modal elements exist
  if (!fullscreenDiv || !fullscreenImg) {
    console.error("Modal elements not found! Ensure #fullscreenImage and #fullscreenImg exist in index.html");
    return;
  }

  if (images.length === 0) {
    console.error("No images found with class 'images'. Check if the class is correctly applied.");
    return;
  }

  images.forEach(img => {
    console.log("Image Found: ", img.src);
    img.addEventListener("click", function () {
      console.log("Clicked Image: ", this.src);
      fullscreenImg.src = this.src;
      fullscreenDiv.style.display = "flex";
    });
  });

  fullscreenDiv.addEventListener("click", function (e) {
    if (e.target !== fullscreenImg) {
      console.log("Closing fullscreen view.");
      fullscreenDiv.style.display = "none";
    }
  });
});
